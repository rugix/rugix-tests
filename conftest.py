import pytest

EXTENDED_ENV = "RUGIX_TESTS_EXTENDED"

# This conftest lives at the bakery project root (next to ``rugix-bakery.toml``).
PROJECT_DIR = Path(__file__).resolve().parent
PRIVATE_KEY = PROJECT_DIR / "keys" / "id_rsa"

sys.path.insert(0, str(PROJECT_DIR))

from harness import BakeryBuilder, build_amd64_vm_config  # noqa: E402

def assert_boot(rugix: RugixCtrl, *, default: str, active: str) -> SystemInfo:
    """Read system info and assert ``default_group`` / ``active_group``."""
    info = rugix.system_info()
    assert info.default_group == default, (
        f"defaultGroup: expected {default!r}, got {info.default_group!r}"
    )
    assert info.active_group == active, (
        f"activeGroup: expected {active!r}, got {info.active_group!r}"
    )
    return info


def install_and_reboot(
    rugix: RugixCtrl,
    source: str,
    *,
    verify: bool = False,
    root_cert: str | None = None,
) -> None:
    """Install from *source*, reboot, and reconnect SSH.

    *source* is forwarded verbatim to ``rugix-ctrl update install`` —
    typically a URL produced by the :func:`bundle_url` fixture, but a
    path on the VM also works. Use ``verify=True`` for signed bundles.

    ``rugix-ctrl --reboot yes`` triggers the reboot itself and the SSH
    connection drops mid-command; the swallowed exception is expected.
    """
    try:
        rugix.update_install(
            source,
            reboot="yes",
            insecure=not verify,
            root_cert=root_cert,
            check=False,
            timeout=600,
        )
    except Exception:
        pass
    rugix.vm.wait_for_reboot()


def _dump_test_artifacts(item: pytest.Item) -> None:
    handle: VMHandle | None = getattr(item, "_vm_handle", None)
    if handle is None:
        return

    outputs_dir = Path(item.config.getoption("--test-outputs-dir"))
    safe_name = item.nodeid.replace("::", "--").replace("/", "--")
    artifact_dir = outputs_dir / safe_name
    artifact_dir.mkdir(parents=True, exist_ok=True)

    serial = handle.serial_output
    if serial:
        (artifact_dir / "serial.log").write_text(serial)

    history = handle.command_history
    if history:
        (artifact_dir / "commands.log").write_text(
            "\n\n".join(str(cmd) for cmd in history) + "\n"
        )


class _QuietHandler(SimpleHTTPRequestHandler):
    def log_message(self, format: str, *args: object) -> None:
        logger.debug("http: " + format, *args)
